double dis[MAX][MAX] , dp[MAX][MAX];
double rec(int lft , int rght)
{
    double &ret=dp[lft][rght];
    if(ret>-.5) return ret;
    if(rght-lft+1 <= 4 ) return 0.0;

    ret= 1.0* oo;
    for(int i=lft+1;i<rght;i+=2)
    {
        for(int j=i+1; j<rght;j+=2 )
        {
            ret= min(ret , rec(lft,i) + rec(i,j) + rec(j,rght) + dis[lft][i] + dis[i][j] + dis[j][rght] );
        }
    }
    return ret;
}

