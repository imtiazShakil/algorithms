typedef struct item * pitem;
struct item {
	int key, prior;
    int cnt;
	pitem  l,  r;
	item() { }
	item (int key, int prior) : key(key), prior(prior), l(NULL), r(NULL) { }
};

int cnt (pitem it) {
	return it ? it->cnt : 0;
}

void upd_cnt (pitem it) {
	if (it)
		it->cnt = cnt(it->l) + cnt(it->r) + 1;
}

void split (pitem t, int key, pitem & l, pitem & r) {
	if (!t)
		l = r = NULL;
	else if (key < t->key)
		split (t->l, key, l, t->l),  r = t;
	else
		split (t->r, key, t->r, r),  l = t;

    upd_cnt(t);
}

void insert (pitem & t, pitem it) {
	if (!t)
		t = it;
	else if (it->prior > t->prior)
		split (t, it->key, it->l, it->r),  t = it;
	else
		insert (it->key < t->key ? t->l : t->r, it);

    upd_cnt(t);
}

void merge (pitem & t, pitem l, pitem r) {
	if (!l || !r)
		t = l ? l : r;
	else if (l->prior > r->prior)
		merge (l->r, l->r, r),  t = l;
	else
		merge (r->l, l, r->l),  t = r;
    upd_cnt(t);
}

int findKey( pitem now , int key)
{
    if( !now ) return false;
    if(key == now->key ) return true;

    if( key < now->key ) return findKey(now->l,key);
    else return findKey(now->r , key);
}

pitem root;
int n;
int actualPosition( int room )
{
    int dead;
    pitem left,right;
    int lo=1,hi=n+20000,mid;
    while(hi>=lo)
    {
        mid=(hi+lo)/2;
        split(root,mid,left,right);

        dead = cnt(left);

        merge(root,left,right);
        if(mid-dead == room && findKey(root,  mid)==false  ) return mid;
        if(mid-dead < room ) lo=mid + 1;
        else hi=mid-1;
    }
    return 0;
}

